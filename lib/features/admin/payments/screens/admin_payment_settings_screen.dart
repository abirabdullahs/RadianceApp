                    materialDefaultAmount: _settings.materialDefaultAmount,
                    examDefaultAmount: _settings.examDefaultAmount,
                    specialDefaultAmount: _settings.specialDefaultAmount,
                    fineDefaultAmount: _settings.fineDefaultAmount,
