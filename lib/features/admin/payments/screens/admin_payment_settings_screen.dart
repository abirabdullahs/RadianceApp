class AdminPaymentSettingsScreen extends ConsumerStatefulWidget {
  const AdminPaymentSettingsScreen({super.key});

  @override
  ConsumerState<AdminPaymentSettingsScreen> createState() =>
      _AdminPaymentSettingsScreenState();
}

class _AdminPaymentSettingsScreenState
    extends ConsumerState<AdminPaymentSettingsScreen> {
  bool _loading = true;
  bool _saving = false;
  PaymentSettingsModel _settings = const PaymentSettingsModel();

  late final TextEditingController _monthlyCtrl;
  late final TextEditingController _admissionCtrl;
  late final TextEditingController _materialCtrl;
  late final TextEditingController _examCtrl;
  late final TextEditingController _specialCtrl;
  late final TextEditingController _fineCtrl;
  late final TextEditingController _dueDayCtrl;
  late final TextEditingController _autoDueDayCtrl;
  late final TextEditingController _bkashCtrl;
  late final TextEditingController _nagadCtrl;
  late final TextEditingController _bankCtrl;

  @override
  void initState() {
    super.initState();
    _monthlyCtrl = TextEditingController();
    _admissionCtrl = TextEditingController();
    _materialCtrl = TextEditingController();
    _examCtrl = TextEditingController();
    _specialCtrl = TextEditingController();
    _fineCtrl = TextEditingController();
    _dueDayCtrl = TextEditingController();
    _autoDueDayCtrl = TextEditingController();
    _bkashCtrl = TextEditingController();
    _nagadCtrl = TextEditingController();
    _bankCtrl = TextEditingController();
    _load();
  }

  @override
  void dispose() {
    _monthlyCtrl.dispose();
    _admissionCtrl.dispose();
    _materialCtrl.dispose();
    _examCtrl.dispose();
    _specialCtrl.dispose();
    _fineCtrl.dispose();
    _dueDayCtrl.dispose();
    _autoDueDayCtrl.dispose();
    _bkashCtrl.dispose();
    _nagadCtrl.dispose();
    _bankCtrl.dispose();
    super.dispose();
  }

  Future<void> _load() async {
    final s = await ref.read(paymentRepositoryProvider).getPaymentSettings();
    if (!mounted) return;
    setState(() {
      _settings = s;
      _monthlyCtrl.text = s.monthlyDefaultAmount.toStringAsFixed(2);
      _admissionCtrl.text = s.admissionDefaultAmount.toStringAsFixed(2);
      _materialCtrl.text = s.materialDefaultAmount.toStringAsFixed(2);
      _examCtrl.text = s.examDefaultAmount.toStringAsFixed(2);
      _specialCtrl.text = s.specialDefaultAmount.toStringAsFixed(2);
      _fineCtrl.text = s.fineDefaultAmount.toStringAsFixed(2);
      _dueDayCtrl.text = s.dueDayOfMonth.toString();
      _autoDueDayCtrl.text = s.autoGenerateDueDay.toString();
      _bkashCtrl.text = s.bkashNumber ?? '';
      _nagadCtrl.text = s.nagadNumber ?? '';
      _bankCtrl.text = s.bankDetails ?? '';
      _loading = false;
    });
  }

  double _num(TextEditingController c, double fallback) =>
      double.tryParse(c.text.trim()) ?? fallback;
  int _int(TextEditingController c, int fallback) =>
      int.tryParse(c.text.trim()) ?? fallback;

  Future<void> _save() async {
    setState(() => _saving = true);
    try {
      final payload = PaymentSettingsModel(
        monthlyDefaultAmount: _num(_monthlyCtrl, _settings.monthlyDefaultAmount),
        admissionDefaultAmount:
            _num(_admissionCtrl, _settings.admissionDefaultAmount),
        materialDefaultAmount: _num(_materialCtrl, _settings.materialDefaultAmount),
        examDefaultAmount: _num(_examCtrl, _settings.examDefaultAmount),
        specialDefaultAmount: _num(_specialCtrl, _settings.specialDefaultAmount),
        fineDefaultAmount: _num(_fineCtrl, _settings.fineDefaultAmount),
        dueDayOfMonth: _int(_dueDayCtrl, _settings.dueDayOfMonth).clamp(1, 31),
        autoGenerateDueDay:
            _int(_autoDueDayCtrl, _settings.autoGenerateDueDay).clamp(1, 31),
        acceptCash: _settings.acceptCash,
        acceptBkash: _settings.acceptBkash,
        acceptNagad: _settings.acceptNagad,
        acceptBank: _settings.acceptBank,
        acceptOther: _settings.acceptOther,
        bkashNumber: _bkashCtrl.text.trim().isEmpty ? null : _bkashCtrl.text.trim(),
        nagadNumber: _nagadCtrl.text.trim().isEmpty ? null : _nagadCtrl.text.trim(),
        bankDetails: _bankCtrl.text.trim().isEmpty ? null : _bankCtrl.text.trim(),
        voucherCenterName: _settings.voucherCenterName,
        voucherAddress: _settings.voucherAddress,
        voucherPhone: _settings.voucherPhone,
        voucherQrEnabled: _settings.voucherQrEnabled,
      );
      final saved = await ref.read(paymentRepositoryProvider).savePaymentSettings(payload);
      if (!mounted) return;
      setState(() {
        _settings = saved;
        _saving = false;
      });
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Payment settings saved', style: GoogleFonts.hindSiliguri())),
      );
    } catch (e) {
      if (!mounted) return;
      setState(() => _saving = false);
      ScaffoldMessenger.of(context).showSnackBar(SnackBar(content: Text('$e')));
    }
  }

